// what is jre in Java?

//     The JRE (Java Runtime Environment) is a software package
//      That provides the minimum requirements for executing Java applications on a computer.
//      It acts as an abstraction layer between the Java program and the
//      Underlying operating system, enabling the "Write Once, Run Anywhere" (WORA) capability.

//     Core Components of JRE
//    - JVM
//    - Java Class Libraries
//    - Class Loader
//    - Configuration files
